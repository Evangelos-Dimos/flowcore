create table products (
    product_id varchar2(20) primary key,
    name varchar2(255),
    product_type varchar2(50),
    quantity int,
    receive_date date DEFAULT sysdate,
    CONSTRAINT chk_product_type 
        CHECK (product_type IN ('laptop', 'tablet', 'smartphone'))
);

create table locations (
    location_id varchar2(20) primary key,
    max_capacity int,
    current_count int default 0
);

create table inventory (
    inventory_id int GENERATED BY DEFAULT AS IDENTITY primary key,
    product_id varchar2(20) unique not NULL,
    product_type varchar2(50),
    location_id varchar2(20),
    quantity_number int,
    foreign key (product_id) references products(product_id),
    foreign key (location_id) references locations(location_id)
);

create table customers (
    customer_id int GENERATED BY DEFAULT AS IDENTITY primary key,
    customer_fname nvarchar2(80),
    customer_lname nvarchar2(80),
    address nvarchar2(40)
);

create table orders (
    order_id int GENERATED BY DEFAULT AS IDENTITY primary key,
    customer_id int,
    status varchar2(20),
    ordered_at DATE DEFAULT sysdate
);

create table order_items (
    order_item_id int GENERATED BY DEFAULT AS IDENTITY primary key,
    order_id int,
    product_id varchar2(20),
    quantity int,
    foreign key (product_id) references inventory(product_id),
    foreign key (order_id) references orders(order_id)
);

CREATE OR REPLACE FUNCTION check_stock (
    p_product_id IN VARCHAR2,
    p_quantity   IN NUMBER
) RETURN NUMBER
IS
    v_stock NUMBER;
BEGIN
    SELECT NVL(SUM(quantity_number), 0)
    INTO v_stock
    FROM inventory
    WHERE product_id = p_product_id;

    IF v_stock >= p_quantity THEN
        RETURN 1;
    ELSE
        RETURN 0;
    END IF;
END;
/


CREATE OR REPLACE PROCEDURE process_order_proc (
    p_order_id IN NUMBER
) AS

    CURSOR order_cursor IS
        SELECT product_id, quantity
        FROM order_items
        WHERE order_id = p_order_id;

    v_stock NUMBER;
    v_success NUMBER := 1;

BEGIN

    -- loop through order items
    FOR rec IN order_cursor LOOP

        -- check stock
        SELECT NVL(SUM(quantity_number), 0)
        INTO v_stock
        FROM inventory
        WHERE product_id = rec.product_id;

        IF v_stock < rec.quantity THEN
            v_success := 0;
            DBMS_OUTPUT.PUT_LINE('Insufficient stock for ' || rec.product_id);
        END IF;

    END LOOP;

    -- if any failed → mark order failed
    IF v_success = 0 THEN
        UPDATE orders
        SET status = 'FAILED'
        WHERE order_id = p_order_id;

        COMMIT;
        RETURN;
    END IF;

    -- otherwise process order
    FOR rec IN order_cursor LOOP

        UPDATE inventory
        SET quantity_number = quantity_number - rec.quantity
        WHERE product_id = rec.product_id;

    END LOOP;

    UPDATE orders
    SET status = 'SHIPPED'
    WHERE order_id = p_order_id;

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('Order processed successfully');

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Order not found');
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error: ' || SQLERRM);
END;
/


CREATE OR REPLACE TRIGGER update_location_count
AFTER INSERT OR UPDATE OR DELETE ON inventory
FOR EACH ROW
BEGIN

    IF INSERTING OR UPDATING THEN
        UPDATE locations
        SET current_count = (
            SELECT NVL(SUM(quantity_number), 0)
            FROM inventory
            WHERE location_id = :NEW.location_id
        )
        WHERE location_id = :NEW.location_id;
    END IF;

    IF DELETING THEN
        UPDATE locations
        SET current_count = (
            SELECT NVL(SUM(quantity_number), 0)
            FROM inventory
            WHERE location_id = :OLD.location_id
        )
        WHERE location_id = :OLD.location_id;
    END IF;

END;
/




